package model;

abstract public class Shape extends AbstractModel {
    private int id;
    private String type;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    abstract public String getTitle();
    abstract public double calculateVolume();
}
